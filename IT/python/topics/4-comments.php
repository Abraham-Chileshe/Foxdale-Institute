                <h3 class="text-primary">What is comments</h3>
                <p>Comments can be used to explain Python code. Comments can be used to make the code more readable. Comments can also be used to prevent execution when testing code.</p>
                <br />

                <h3 class="text-primary">Creating a comment</h3>
                <p>Comments starts with a #, anything after this symbol will be considered part of the comment</p>
            
<pre>
<code class="language-python">  
#This is a comment
print(&quot;Hello, World!&quot;)
</code>
</pre>

                <p>Comments can be placed at the end of a line, and Python will ignore the rest of the line:</p>
<pre>
<code class="language-python">  
print("Hello, World!") #This is a comment
</code>
</pre>

                <p>A comment does not have to be text that explains the code, it can also be used to prevent Python from executing code:</p>
<pre>
<code class="language-python">  
#print(&quot;Hello, World!&quot;)
print(&quot;Cheers, Mate!&quot;)
</code>
</pre><br/>

